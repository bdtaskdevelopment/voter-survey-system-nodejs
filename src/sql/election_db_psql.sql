CREATE OR REPLACE FUNCTION insert_bulk_users(total_users BIGINT)
RETURNS void AS $$
BEGIN
    INSERT INTO tbl_user (
        password, password_expiry, full_name_eng, user_login, mobile, address,
        userrole_id, data_map_id, file_id, is_active
    )
    SELECT
        md5('password' || seq) AS password,
        CURRENT_DATE + (floor(random() * 365) * INTERVAL '1 day') AS password_expiry,
        'ব্যবহারকারী ' || seq AS full_name_eng,
        'user' || seq AS user_login,
        '017' || LPAD(seq::TEXT, 8, '0') AS mobile,
        'Address ' || seq AS address,
        NULL::INT AS userrole_id,
        (floor(random() * 1000) + 1)::INT AS data_map_id,
        (floor(random() * 5000) + 1)::INT AS file_id,
        true AS is_active
    FROM generate_series(1, total_users) AS seq;
END;
$$ LANGUAGE plpgsql;

